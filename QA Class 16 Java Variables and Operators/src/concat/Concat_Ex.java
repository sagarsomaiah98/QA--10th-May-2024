package concat;

public class Concat_Ex {

	public static void main(String[] args) {
		
		int age=65;
		double salary=345.61;
		String name="Lee";
		
		System.out.println(age+(salary+name));
		
		//salary = 345.61
		
		System.out.println("salary = "+salary);
		
		// Lee whose age is 65 has salary of 345.61 dollars

	}

}
